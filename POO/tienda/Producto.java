package POO.tienda;



public class Producto {
    //propiedades
    String nombre;
    double precio;
    String fechaVencimiento;

    //constructor de la clase
    public Producto(String nombre, double precio, String fechaVencimiento) {
        this.nombre = nombre;
        this.precio = precio;
        this.fechaVencimiento = fechaVencimiento;
    }
    

}
